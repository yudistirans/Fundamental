user_input_p = input("Berapa panjang persegi panjang : ")
user_input_l = input("Berapa lebar persegi panjang: ")

panjang = int(user_input_p)
lebar = float(user_input_l)
luas = panjang * lebar

print(f"Luas persegi panjang dengan panjang {panjang} dan lebar {lebar} adalah {luas:.2f}")

